let a = 5; //0101
let b = 3; //0011

// BITWISE AND OPERATOR 
console.log(a & b); // 1 0001

//BITWISE OR OPERATOR
console.log(a | b); // 0111 =>7

//BIWISE XOR OPERATOER
console.log(a ^ b); //0110 -> 6

// BITWIS ENOT OPERATOR
console.log(~a); // -6

//(Left shift)
console.log(a << 1); // 10 

//(Right shift)
console.log(a >> 1); // 2  


